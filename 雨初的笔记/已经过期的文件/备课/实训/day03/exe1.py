#打印图形
'''
用户输入3时 
*****   i=1   2*row-1-(i-1)*2
@***    i=2   2*row-1-2
@@*     i=3   2*row-1-4
用户输入4时
*******       2*row-1-0
@*****        2*row-1-2
@@***         2*row-1-4
@@@*          2*row-1-6
'''
row = int(input('请输入要打印的行数:'))
for i in range(1,row+1):
    print('@'*(i-1)+'*'*(2*row-1-(i-1)*2))



